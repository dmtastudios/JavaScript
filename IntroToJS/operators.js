// OPERATORS
// Addition and Subtraction (+ is for Addition. - is for Subtraction)
console.log(2+2); // 4 
console.log(2.25+2.55); // 6.3
console.log(1 -3); // -2

console.log(4*5);
console.log(1.5*2.5);
console.log(1/4);

// Exponentiation (** is for Exp)
console.log(6**2);

// Modulus (AKA Remainder)
// Return the division remainder
// Percentage Sign % is used for Modulus

console.log(10%3); //1
console.log(14%4); //2
console.log(3%5); //3
// Since 5 cannot got into 3, the remainder is 3
console.log(3%2);

// order of operations in JS (PEMDAS=PEMDMAS - includes modulus)
console.log(1+2*3); //7
console.log((1+2)*3); //9

